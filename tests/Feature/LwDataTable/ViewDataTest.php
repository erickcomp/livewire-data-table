// --- Merging behavior ---

it('merges static and dynamic view data with dynamic taking precedence', function () {
    $dataTable = new DataTable(
        dataSrc: collect([['id' => 1, 'name' => 'Product']]),
        paginationView: 'bootstrap',
        staticViewData: ['label' => 'static-value', 'shared' => 'from-static'],
        dynamicViewData: ['label' => 'dynamic-value'],
    );

    $dataTable->columns->push(viewDataColumn('Name', 'name'));
    $dataTable->columns->push(CustomRenderedColumn::fromComponentAttributeBag(
        new ComponentAttributeBag(['title' => 'Info', 'data-field' => 'id']),
        customRendererCode: '{{ $label }} | {{ $shared }}',
    ));

    $component = Livewire::test(LwDataTable::class, ['data-table' => $dataTable]);

    $component->assertSee('dynamic-value | from-static');
});

it('does not send static view data over the wire', function () {
    $dataTable = new DataTable(
        dataSrc: collect([['id' => 1, 'name' => 'Product']]),
        paginationView: 'bootstrap',
        staticViewData: ['config' => 'server-only'],
    );

    $dataTable->columns->push(viewDataColumn('Name', 'name'));

    $component = Livewire::test(LwDataTable::class, ['data-table' => $dataTable]);

    expect($component->get('vd'))->toBe('');
});
